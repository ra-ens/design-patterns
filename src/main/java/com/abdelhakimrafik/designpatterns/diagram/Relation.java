package com.abdelhakimrafik.designpatterns.diagram;

public class Relation {
    private String name;

    public Relation(String name) {
        this.name = name;
    }

    public Relation() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
